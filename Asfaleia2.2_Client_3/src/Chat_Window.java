    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        for (int i = 0; i < Ports.size(); i++) {

            try {
                String text = jTextArea1.getText();
                Message mes = new Message(text, "Chat_Message");
                ObjectOutputStream OUT = new ObjectOutputStream(new Socket("127.0.0.1", parseInt(Ports.get(i).get(i).getPort())).getOutputStream());
                OUT.writeObject(mes);
                OUT.flush();

            } catch (IOException ex) {
                Logger.getLogger(Chat_Window.class.getName()).log(Level.SEVERE, null, ex);
            } 
        }

    }//GEN-LAST:event_jButton1ActionPerformed

